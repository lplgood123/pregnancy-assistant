def strip_prefix(text: str, prefix: str) -> str:
    t = str(text or '').strip()
    if t.startswith(prefix):
        return t[len(prefix):].strip()
    if t.startswith(prefix.replace("：", ":")):
        return t[len(prefix.replace("：", ":")) :].strip()
    return t


def load_daily() -> list[dict]:
    raw = DAILY_SOURCE.read_text(encoding='utf-8')
    # 源文件并非严格 JSON（可能包含多个数组拼接），按对象片段逐个解析更稳妥。
    object_chunks = re.findall(r'\{[\s\S]*?\}', raw)
    by_day: dict[int, dict] = {}
    for chunk in object_chunks:
        try:
            item = json.loads(chunk)
        except Exception:
            continue
        if not isinstance(item, dict):
            continue
        d = int(item.get('d', 0))
        if d <= 0:
            continue
        by_day[d] = {
            'd': d,
            'w': str(item.get('w', '')).strip(),
            'b': strip_prefix(item.get('b', ''), '宝宝变化：'),
            'm': strip_prefix(item.get('m', ''), '妈妈变化：'),
        }

    out = sorted(by_day.values(), key=lambda x: x['d'])
    return out


